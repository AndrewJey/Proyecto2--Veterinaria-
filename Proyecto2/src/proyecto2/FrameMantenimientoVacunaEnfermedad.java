    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        if (tipo == 1) {
            String datos = "";
            datos = "('" + this.codEnfermedad.getText() + "','" + this.codVacuna.getText() + "','"
                    + this.porcentajeE.getText() + "','" + this.not.getText() + "')";
            this.CP.insertar("\"schProyecto\".enfermedad_vacuna", datos);
            codVacuna.setEditable(false);
            codEnfermedad.setEditable(false);
            porcentajeE.setEditable(false);
            not.setEditable(false);
            correr();
        } else if (tipo == 2) {
            String estado;
            String datos;
            datos = "set \"porc_efectividad\" = '" + this.porcentajeE.getText()
                    + "' where \"cod_vacuna\" = '" + this.codVacuna.getText() + "'";
            estado = this.CP.modificar("\"schProyecto\".enfermedad_vacuna", datos);
            datos = "set \"notas\" = '" + this.not.getText()
                    + "' where \"cod_vacuna\" = '" + this.codVacuna.getText() + "'" + "and" + "\"cod_vacuna\" = '" + this.codVacuna.getText() + "'";
            estado = this.CP.modificar("\"schProyecto\".enfermedad_vacuna", datos);
            codVacuna.setEditable(false);
            codEnfermedad.setEditable(false);
            porcentajeE.setEditable(false);
            not.setEditable(false);
            this.correr();
        } else if (tipo == 3) {
            String estado;
            String datos;
            datos = " where cod_vacuna = " + "'" + this.codVacuna.getText() + "'" + " and " + "cod_vacuna = " + "'" + this.codVacuna.getText() + "'";
            estado = this.CP.borrar("\"schProyecto\".enfermedad_vacuna", datos);
            codVacuna.setText("");
            not.setText("");
            codEnfermedad.setText("");
            porcentajeE.setText("");
            correr();
        }
        tipo = 0;
    }//GEN-LAST:event_okActionPerformed

