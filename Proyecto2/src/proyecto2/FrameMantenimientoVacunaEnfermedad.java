    public void Select() {
        String codE = "";
        String codV = "";
        String pEfecti = "";
        String notas = "";
        try {
            ResultSet datos = CP.select("*", "\"schProyecto\".\"enfermedad_vacuna\"", "");
            while (datos.next()) {
                codE = datos.getString(1);
                codV = datos.getString(2);
                pEfecti = datos.getString(3);
                notas = datos.getString(4);
                Object datostabla[] = {codE, codV, pEfecti, notas};
                modelo.addRow(datostabla);
            }
        } catch (Exception e) {
        }
    }

