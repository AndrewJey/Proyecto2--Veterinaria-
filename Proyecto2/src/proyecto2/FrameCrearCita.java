    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        if (tipo == 1) {
            String datos = "";
            datos = "('" + this.cod.getText() + "','"
                    + this.codMascota.getSelectedItem().toString() + "','" + dia.getSelectedItem().toString() + "/" + (mes.getSelectedIndex() + 1) + "/" + anno.getSelectedItem().toString() + "')";
            this.CP.insertar("\"schProyecto\".cita", datos);
            cod.setEditable(false);
            codMascota.setEnabled(false);
            nomMascota.setEnabled(false);
            anno.setEnabled(false);
            mes.setEnabled(false);
            dia.setEnabled(false);
            correr();
        } else if (tipo == 2) {
            String estado;
            String datos;
            datos = "set \"cod_mascota\" = '" + this.codMascota.getSelectedItem().toString()
                    + "' where \"cod\" = '" + this.cod.getText() + "'";
            estado = this.CP.modificar("\"schProyecto\".cita", datos);
            datos = "set \"fecha_cita\" = '" + dia.getSelectedItem().toString() + "/" + (mes.getSelectedIndex() + 1) + "/" + anno.getSelectedItem().toString()
                    + "' where \"cod\" = '" + this.cod.getText() + "'";
            estado = this.CP.modificar("\"schProyecto\".cita", datos);
            cod.setEditable(false);
            codMascota.setEnabled(false);
            nomMascota.setEnabled(false);
            anno.setEnabled(false);
            mes.setEnabled(false);
            dia.setEnabled(false);
            this.correr();
        } else if (tipo == 3) {
            String estado;
            String datos;
            datos = " where cedula = " + "'" + this.cod.getText() + "'";
            estado = this.CP.borrar("\"schProyecto\".dueno", datos);
            correr();
            cod.setText("");
        }
        tipo = 0;
    }//GEN-LAST:event_okActionPerformed

