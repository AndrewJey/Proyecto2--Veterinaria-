    public void comboMascota() throws SQLException {
        ResultSet datos = CP.select("cod_mascota", "\"schProyecto\".\"mascota\"", "");
        ResultSet datos2 = CP.select("nombre", "\"schProyecto\".\"mascota\"", "");
        String a = "";
        String b = "";
        while (datos.next()) {
            a = datos.getString(1);
            if (datos2.next()) {
                b = datos2.getString(1);
            }
            codMascota.addItem(a);
            nomMascota.addItem(b);
        }
    }

    public void correr() {
        String principal[] = {"Código Cita", "Código Mascota", "Fecha"};
        String datos[][] = {};
        modelo = new DefaultTableModel(datos, principal);
        tablaCita.setModel(modelo);
        this.Select();
    }

    public void llenar() {
        cod.setText((String) modelo.getValueAt(0, 0));
    }

    public void Select() {
        String codC = "";
        String fecha = "";
        String codM = "";
        try {
            ResultSet datos = CP.select("*", "\"schProyecto\".\"cita\"", "");
            while (datos.next()) {
                codC = datos.getString(1);
                codM = datos.getString(2);
                fecha = datos.getString(3);
                Object datostabla[] = {codC, codM, fecha};
                modelo.addRow(datostabla);
            }
        } catch (Exception e) {
        }
    }

