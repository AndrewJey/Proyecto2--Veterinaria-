    private void tablaCitaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaCitaMouseClicked
        cod.setText((String) modelo.getValueAt(tablaCita.getSelectedRow(), 0));
        int ind = 0;
        for (int i = 0; i < codMascota.getItemCount(); i++) {
            codMascota.setSelectedIndex(i);
            String b = (String) modelo.getValueAt(tablaCita.getSelectedRow(), 3);
            if (b.equalsIgnoreCase(codMascota.getSelectedItem().toString())) {
                ind = i;
            }
        }
        codMascota.setSelectedIndex(ind);
        nomMascota.setSelectedIndex(ind);
    }//GEN-LAST:event_tablaCitaMouseClicked

